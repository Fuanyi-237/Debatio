export const useI18n = create<I18nState>()(
  persist(
    (set, get) => ({
      language: 'en',
      setLanguage: (lang: Language) => set({ language: lang }),
      t: (key: string) => {
        const { language } = get()
        return translations[language]?.[key] || translations['en']?.[key] || key
      },
    }),
    {
      name: 'i18n-storage',
    }
  )
)

